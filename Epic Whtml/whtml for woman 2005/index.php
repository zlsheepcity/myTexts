Непролетарске методы вы уже держите в уме: выделить это значит обрамить енота тегом <u>&lt;B&gt;</u> и соответсвующую ячейку писать как:<br/>
<div class="cd"><u>&lt;TD&gt;</u><u>&lt;B&gt;</u>Енот<u>&lt;/B&gt;</u><u>&lt;/TD&gt;</u></div>
