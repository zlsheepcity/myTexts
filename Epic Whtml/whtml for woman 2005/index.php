Как-то так.
<br/><br/>	

<h3>Обрамлять</h3>

Конечно же, всё волшебство заключается в том, какие именно «оп!» используются. Заклинания называются <i class="name">тэгами</i> (tags). Их много, но та, кто познает все теги мира — становится Великой Чародейкой и получает Нестареющую Красоту в награду. Значит оно того стоит.<br/><br/>

Чтобы не путать теги с обычным текстом, пишутся они английскими буквами внутри <u>&lt;</u> <u>&gt;</u> вот таких треугольных скобочек. Например, английская буква <b>B</b> заставит все буквы стать жирными (bold), что твой кот:<br/>
<div class="cd"><u>&lt;B&gt;</u> <b>Видите, мы как бы сказали «толстей!!!»,</b></div><br/>

<b>и все потолстело.</b><br/><br/>

<b>И тут же мы с вами убеждаемся, что если вовремя не закончить волшебство, то толстеть прийдётся всю жизнь. Как и в сказке про «горшочек вари и горшочек не вари», на помощь нам приходят <i class="name">закрывающие теги</i>:</b><br/>
<div class="cd"><u>&lt;/B&gt;</u> Видите, мы как бы сказали «горшочек не вари».</div><br/><br/>

Закрывающие теги отличаются только лишь косой чертой, перед заклинанием. Вот и получается, что наши слова и предложения мы вынуждены писать между открывающим и закрывающим тегами. Мудрые женщины называют этот процесс <i class="name">обрамлением</i> (obramlenie):<br/>
<div class="cd"><u>&lt;TAG&gt;</u> дорогая, обрамите, пожалуйста, эти туфельки <u>&lt;/TAG&gt;</u></div><br/><br/>

Теперь вы можете хорошенько выспаться, чтобы с понедельника начать новую жизнь. Полную удивительных заклинаний, тегов и метатегов.
<br/><br/>

