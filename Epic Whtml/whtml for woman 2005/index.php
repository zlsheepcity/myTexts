<a name="intro" class="anchor"><h2 class="p"><span>§1. </span>Введение</h2></a>

<h3>Абсолютное волшебство</h3>
Обычный текст, который вы можете напечатать на вашем компьютере потому и зовётся обычным, что никаких чудес не содержит — только буквы, цифры и :-)<br/><br/>

Разумеется, для спасения мира одних смайликов недостаточно. Тёмные джедаи выбирают странный путь, на котором в пучинах <i>майкрософт ворда</i>, <i>фотошопа</i> и другими демонами рисуются слова самых кошмарных форм и самых мрачных оттенков радуги. Истинные волшебницы — те, кто милы и очаровательны — познают тайны <i class="name">хтмл</i> (HTML).

Тайны <i class="name">хтмл</i> заключаются в том, что мы пишем обычный текст, но в нужный момент вставляем заклинание, и текст преображается. Мы как бы говорим «ОП!», и все происходит.<br/>

