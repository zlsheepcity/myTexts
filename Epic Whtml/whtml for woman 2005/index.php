<a name="saturday" class="anchor"><h2 class="p"><span>§7. </span>Суббота: подведение итогов и что дальше</h2></a>
Давайте окинем взглядом наши нынешние возможности.<br/><br/>

Например, мы можем написать отличный пост в ЖЖ. Но не просто написать <i>«Ой, девченки! Вчера была такая классная вечеринка!»</i>, а все подробно и досканально доложить. Кто куда смотрел, кто сколько ел и пил, о чем говорили в туалете. И все это с хронологическим перечислением, с полным анализом, со сводными таблицами и даже с графиками (если предварительно нарисовать их в фотошопе).<br/><br/>

Мы еще не можем сделать свой сайт, но мы уже начинаем об это думать. Впрочем…<br/><br/>

<div class="cd block" style="font-size:.9em;width:600px;">
	&lt;HTML&gt;<br/>
	&nbsp;&nbsp; &lt;HEAD&gt;<br/>
	&nbsp;&nbsp;&nbsp;&nbsp; &lt;TITLE&gt;Мой сайт&lt;/TITLE&gt;<br/>
	&nbsp;&nbsp;&nbsp;&nbsp; &lt;META HTTP-EQUIV="Content-Type" CONTENT="text/html; charset=1251" /&gt;<br/>
	&nbsp;&nbsp; &lt;/HEAD&gt;<br/>
	&nbsp;&nbsp; &lt;BODY&gt;<br/>
	&nbsp;&nbsp;&nbsp;&nbsp; &lt;H1&gt;Я хочу написать, что:&lt;/H1&gt;<br/>
	&nbsp;&nbsp;&nbsp;&nbsp; …<br/>
	&nbsp;&nbsp; &lt;/BODY&gt;<br/>
	&lt;/HTML&gt;<br/>
</div><br/>

Вот вам все необходимые обрамления для простейшего сайта. Вставьте вместо троеточия все, о чем вы хотите написать (нумерованным списком, с таблицами и картинками) сохраните получившийся текстовой файл с расширением <u>.html</u> и засовывайте в интернет. (В самой страшной строчке из показанных — мы просто постарались сказать, что пишем на русском языке. На всякий случай.)<br/><br/>

Соседка относится к нам с гораздо большим уважением. Это&nbsp;факт.<br/><br/>

В конце концов, мы теперь обрамляем все. Даже то, что не относится к хтмл.<br/><br/>

Но давайте не будем забывать главной задачи, поставленной перед началом обучения. <b>С этих пор на вверенных вам сайтах будут царить чистота и порядок.</b> Босс на семь, или даже на девять процентов поднимет вам жалование, а те ребята из отдела программистов будут бегать к вам с цветами.<br/> 
Не забывайте хтмл, берегите его.
