	<u>ОП!</u> <b style="color:red;background:lightblue">на голубом!</b><br/>
	<u>ОП!</u> 
	<nobr><b style="color:red">а</b><b style="color:indigo">б</b><b>с</b><b style="color:green">о</b><b style="color:red">л</b><b style="color:blue">ю</b><b style="color:green">т</b><b>н</b><b style="color:orange">о</b><b style="color:indigo">е</b></nobr>
