		<u>&lt;I&gt;</u> <u>&lt;B&gt;</u>текст<u>&lt;/B&gt;</u> <u>&lt;/I&gt;</u><br/>
		Почуствуйте разницу!<br/><br/>
		(<i>подсказка:</i> нет разницы)<br/><br/>
	</li>
	<li>
		<b>Искаверкайте очередность открывающих и закрывающих тегов:</b><br/>
		<u>&lt;B&gt;</u> <u>&lt;I&gt;</u>текст<u>&lt;/B&gt;</u> <u>&lt;/I&gt;</u><br/><br/>
		(<i>подсказка:</i> никогда так не делайте)<br/><br/>
	</li>
	<li>
		<b>Напишите три тысячи постов в ЖЖ</b><br/>
		Это не шутка. Только упорными практическими занятиями можно заставить себя пользовать теги. О чем бы вы ни писали, рано или поздно они (теги) появятся в вашем тексте. А вы увидите собственными глазами во что они превращаются для читателей.<br/><br/>
	</li>	
</ul>

