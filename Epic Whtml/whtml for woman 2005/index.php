Как только написали всех енотов, каждого в своей ячейке — <u>&lt;TD&gt;</u>Енот<u>&lt;/TD&gt;</u> — закрываем строчку тегом <u>&lt;/TR&gt;</u> и открываем следующую.<br/><br/>

Изнуряет, выматывает, бесит и плющит. Да, это хтмл, детка! Это темная сторона его силы.
<br/><br/>


<h3>Соблюдая количество ячеек</h3>
Логично, что в каждой строчке нашей таблицы, должно быть одинаковое количество ячеек (что в итоге на экране превращается в количество колонок). Посмотрите на наш звериный пример — видите, самая первая ячейка у нас пустая, и мы вынуждены были написать пустую пару <u>&lt;TD&gt;&lt;/TD&gt;</u> в первой строчке таблицы.<br/><br/>

Если бы мы этого не сделали, у нас бы сдвинулись ячейки. А если наша таблица была бы размером сто на сто, и если бы мы забыли написать пустую пару тегов где-то в середине этой таблицы, то сдвинулись бы не только ячейки но и полушария мозга.
<br/><br/>


