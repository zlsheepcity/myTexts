<blockquote class="offtop">
<h3 style="margin-top:0;">«А как мне положить картинку в интернет?»</h3>
Для мужчин, заставить женщину прекратить постить картинки в жж, все равно, что заставить эту же женщину замолчать во время футбольного матча. То есть — неосуществимая мечта. И мужчины постоянно создают сложности для самовыражения.<br/><br/>

«Как мне положить картинку в интернет?» — самый популярный вопрос вставших на путь хтмл. Признаюсь, что вопрос достаточно сложный, на пальцах не покажешь.<br/><br/>

Когда картинка лежит на вашем компьютере — это <i>файл,</i> например файл с именем supermodel.jpg<br/><br/>

Для того чтобы показывать файл в интернете у него должен быть адрес. Точно также, как у любого сайта в интернете есть адрес, так и любой файл может его иметь. Для этого файл надо положить на <i>сервер.</i><br/><br/>

Здесь и начинаются трудности. Поскольку иметь доступ на сервер — это как идеальная фигура — не у каждой есть (но каждая может получить).<br/><br/>

<ol>
