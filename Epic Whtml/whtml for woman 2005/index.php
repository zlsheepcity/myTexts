<a name="wednesday" class="anchor"><h2 class="p"><span>§4. </span>Среда: далее по списку</h2></a>
Списки — это моё любимое волшебство<br/><br/>

<div class="cd block">
	<h4 style="margin-top:0">Что делают списки?</h4>
	<ol>
		<li>ласкают глаз
		<li>разбавляют серость
		<li>творят добро
		<li>и многое другое
	</ol>
</div><br/>

Создаются списки с помощью тега <u>&lt;OL&gt;</u> (ordered list), а помогает ему тег <u>&lt;LI&gt;</u> (list item):<br/><br/>

<div class="cd block">
	<u>&lt;OL&gt;</u><br/>
	<u>&lt;LI&gt;</u>ласкают глаз<br/>
	<u>&lt;LI&gt;</u>разбавляют серость<br/>
	<u>&lt;LI&gt;</u>творят добро<br/>
	<u>&lt;LI&gt;</u>и многое другое<br/>
	<u>&lt;/OL&gt;</u><br/>
</div>
Причем первый тег как бы говорит нам «Привет, сейчас будет список». А второй отвечает «Оп! Пунктик! Оп! Еще один!»<br/><br/>

<h3>Это делают роботы</h3>
Пока не было хтмл, женщины ненавидили узнавать опосля, что первым пунктом должна идти фраза «объясняют суть». Ведь приходилось брать уже написаный список, вставлять строчку «1. объясняют суть», после чего менять старую цифру 1 на цифру 2, старую цифру 2 на 3 и так далее.<br/><br/>

Современные технологии позволяют нам не заниматься такими глупостями. Мы просто вставим строчку<br/> 
<div class="cd"><u>&lt;LI&gt;</u>объясняют суть</div><br/>
в нужное место, и получим результат, в котором несчастный робот поменял цифры вместо нас:<br/><br/>

<div class="cd block">
	<h4 style="margin-top:0">Что делают списки?</h4>
	<ol>
		<li>объясняют суть
		<li>ласкают глаз
		<li>разбавляют серость
		<li>творят добро
		<li>и многое другое
	</ol>
</div><br/><br/>

Бывают еще случаи, когда мы отвлеклись на новую серию Доктора Хауса, а потом продолжили писать список. Например с пятого пункта. Короткое дополнение к тегу <u>&lt;OL&gt;</u> радует нас:<br/><br/>

<div class="cd block" style="width:600px;">
<table style="border:0;width:600px;">
	<tr>
		<td style="vertical-align:top;width:45%;">
			<u>&lt;OL start=5&gt;</u><br/>
			<u>&lt;LI&gt;</u>творят добро<br/>
			<u>&lt;LI&gt;</u>и многое другое<br/>
			<u>&lt;/OL&gt;</u><br/>
		</td>
		<td style="width:10%;">
			<b>=</b>
		</td>
		<td style="vertical-align:top;width:45%;">
			<br/>
			<ol start="5">
				<li>творят добро
				<li>и многое другое
			</ol>
			<br/>
		</td>
	</tr>
</table></div><br/><br/>

Но еще бывают случаи, когда кто-то с детства ненавидит цифры. И опять короткое дополнение радует:<br/><br/>

<div class="cd block" style="width:600px;">
<table style="border:0;width:600px;">
	<tr>
		<td style="vertical-align:top;width:45%;">
			<u>&lt;OL type=a&gt;</u><br/>
			<u>&lt;LI&gt;</u>объясняют суть<br/>
			<u>&lt;LI&gt;</u>ласкают глаз<br/>
			<u>&lt;LI&gt;</u>разбавляют серость<br/>
			<u>&lt;LI&gt;</u>творят добро<br/>
			<u>&lt;/OL&gt;</u><br/>
		</td>
		<td style="width:10%;">
			<b>=</b>
		</td>
		<td style="vertical-align:top;width:45%;">
			<br/>
			<ol type="a">
				<li>объясняют суть
				<li>ласкают глаз
				<li>разбавляют серость
				<li>творят добро
			</ol>
			<br/>
		</td>
	</tr>
</table></div><br/>


Можно писать <u>&lt;OL type=a&gt;</u>, еще можно писать <u>&lt;OL type=A&gt;</u> (и тогда список будет нумероваться заглавными буквами), а&nbsp;еще можно писать <u>&lt;OL type=I&gt;</u>:<br/><br/>

<div class="cd block">
	<ol type="I">
		<li>объясняют суть
		<li>ласкают глаз
		<li>разбавляют серость
		<li>творят добро
	</ol>
</div><br/>

Русских букв буржуи не знают, но всегда можно попробовать. Вдруг вы читаете эту книгу через сто лет.
<br/><br/>

<h3>Балет буллетов</h3>

