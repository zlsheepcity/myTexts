   <li>Попробуйте для начала полностью прочитать книгу «ХТМЛ для Женщин», затем дождаться и освоить книги «Протоколы передачи данных для Женщин», «Серверное администрирование для Женщин», «Логин и Пассворд для Женщин». После чего заведите себе собственный сервер.</li>
</ol>

