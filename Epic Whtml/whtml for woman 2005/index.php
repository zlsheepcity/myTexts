	</table>
</div><br/><br/>

Как-то все кривенько. Давайте добавим таблице рамку. Для этого напишем волшебный <u>border</u> в теге <u>&lt;TABLE&gt;</u><br/><br/>

<div class="cd block">
		<u>&lt;TABLE border=1&gt;</u><br/>
		…<br/>
</div><br/>

<div class="cd block">
	<table border="1">
		<tr><td></td><th>Собака</th><th>Кот</th><th>Енот</th></tr>
		<tr><td>Лает:</td><td>Да</td><td>Нет</td><td>Да</td></tr>		
		<tr><td>Кусает:</td><td>Да</td><td>Да</td><td>Да</td></tr>				
		<tr><td>Спит:</td><td>Иногда</td><td>Всегда</td><td>Никогда</td></tr>				
	</table>
</div><br/><br/>

Енотам не нравится теснота. Напишем для того же <u>&lt;TABLE&gt;</u> волшебный <u>cellspacing</u> — он позволит нам управлять расстоянием между ячейками:
<br/><br/>

<div class="cd block">
		<u>&lt;TABLE border=1 cellspacing=10&gt;</u><br/>
		…<br/>
</div><br/>

<div class="cd block">
	<table border="1" cellspacing="10">
		<tr><td></td><th>Собака</th><th>Кот</th><th>Енот</th></tr>
		<tr><td>Лает:</td><td>Да</td><td>Нет</td><td>Да</td></tr>		
		<tr><td>Кусает:</td><td>Да</td><td>Да</td><td>Да</td></tr>				
		<tr><td>Спит:</td><td>Иногда</td><td>Всегда</td><td>Никогда</td></tr>				
	</table>
</div><br/><br/>

Теперь енотам не нравится то, что коты-еноты вместе с ответами да-иногда прилипают к стенкам. Удовлетворяя енотов, нам прийдется каждой(!) ячейке с ответами добавить волшебную фразу <u>align=center</u>:<br/><br/>

<div class="cd block">
		…<br/>
		<u>&lt;TD align=center&gt;</u>Да<u>&lt;/TD&gt;</u><br/>
		…<br/>
</div><br/>

<div class="cd block">
	<table border="1" cellspacing="10">
		<tr><td></td><th align=center>Собака</th><th align=center>Кот</th><th align=center>Енот</th></tr>
		<tr><th>Лает:</th><td align=center>Да</td><td align=center>Нет</td><td align=center>Да</td></tr>		
		<tr><th>Кусает:</th><td align=center>Да</td><td align=center>Да</td><td align=center>Да</td></tr>				
		<tr><th>Спит:</th><td align=center>Иногда</td><td align=center>Всегда</td><td align=center>Никогда</td></tr>				
	</table>
</div><br/>
(а еще я незаметно поменял <u>&lt;TH&gt;</u> вместо <u>&lt;TD&gt;</u> для лает-спит-кусает в первом столбике)
<br/><br/>

<h3>Совмещение ячеек</h3>

Всем своим видом, наша таблица как бы говорит нам: «Кусают все!». Говорит настолько явно, что нам хочется избежать повторяющихся да-да-да в третьей строчке. Мы сделаем это очередным волшебством, и вместо:<br/><br/>

<div class="cd block">
		…<br/>
		<u>&lt;TR&gt;</u><br/>
		<u>&lt;TH&gt;</u>Кусает:<u>&lt;/TH&gt;</u><br/>
		<u>&lt;TD align=center&gt;</u>Да<u>&lt;/TD&gt;</u><br/>
		<u>&lt;TD align=center&gt;</u>Да<u>&lt;/TD&gt;</u><br/>
		<u>&lt;TD align=center&gt;</u>Да<u>&lt;/TD&gt;</u><br/>
		<u>&lt;/TR&gt;</u><br/>
		…<br/>
</div>
напишем:
<div class="cd block">
		…<br/>
		<u>&lt;TR&gt;</u><br/>
		<u>&lt;TD&gt;</u>Кусает:<u>&lt;/TD&gt;</u><br/>
		<u>&lt;TD colspan=3 align=center&gt;</u>Кусают все!<u>&lt;/TD&gt;</u><br/>
		<u>&lt;/TR&gt;</u><br/>
		…<br/>
</div><br/>

Волшебная фраза <u>colspan</u> заставляет одну ячейку растянуться на три ячейки (именно это число мы и написали). Получается, что в третей строчке у нас всего две ячейки, но их как бы в сумме четыре. Попробуйте представить, что это как бы очень просто.<br/><br/> 

<div class="cd block">
	<table border="1" cellspacing="10">
		<tr><td></td><th align=center>Собака</th><th align=center>Кот</th><th align=center>Енот</th></tr>
		<tr><th>Лает:</th><td align=center>Да</td><td align=center>Нет</td><td align=center>Да</td></tr>		
		<tr><th>Кусает:</th><td colspan="3" align=center>Кусают все!</td></tr>				
		<tr><th>Спит:</th><td align=center>Иногда</td><td align=center>Всегда</td><td align=center>Никогда</td></tr>				
	</table>
</div><br/>

<blockquote class="offtop">
А еще можно соединять ячейки по вертикали. Но давайте не будем об этом… Впрочем я вам скажу волшебное слово <u>rowspan</u>, а вы уже сами делайте что хотите.
</blockquote>
<br/>

<h3>Удовлетворительный результат</h3>
