	<h6 class="sh" style="margin-bottom:0"><u class="fixsize">&lt;H6&gt;</u>Принц-зонтик<u class="fixsize">&lt;/H6&gt;</u></h6>
</div><br/>
Можно выбрать заголовок (header) и для всей книги, и для главы, и для эпизода — должно хватить на всех.
<br/><br/>

<h3>Тег параграфа</h3>
А еще было бы здорово сам текст сказки разбить на небольшие параграфы — чтобы глаза не терялись.<br/><br/>

<div class="cd block">
<p style="margin-top:0"><u>&lt;P&gt;</u>Однажды наказала матушка Аленушке сходит в магазин, купить баранок, сметаны и икры красной. Взяла Аленушка зонтик, одела праздничные туфельки и вышла на улицу.<u>&lt;/P&gt;</u></p>
<p style="margin-bottom:0"><u>&lt;P&gt;</u>А на улице ярко-ярко! От солнца в небе глаза слезятся, деревенские мальчишки все в очках темных, барышни телеса богатые в купальники прячут. Смотрит Аленушка на небо без единого облачка и говорит зонтику: «Зачем я только взяла тебя, неподумавши. Засмеют теперь меня люди. Надо мне тебя выкинуть подобру-поздорову.»<u>&lt;/P&gt;</u></p>
</div><br/> 

Этот тег <u>&lt;P&gt;</u> (paragraph) ничего больше и не делает, как просто напросто отделяет один кусок текста от второго. Он даже красную строку почти никогда не делает — а в школе всегда надо было. Но у этого тега, как и у тега для заголовков есть одна важная особенность…
<br/><br/>


<h3>Жадность пространства</h3>

Если вы попробуете в строчку написать:<br/>
<div style="width:600px;">
<div class="cd">
<u>&lt;H3&gt;</u>Привет!<u>&lt;/H3&gt;</u> <u>&lt;P&gt;</u>А что ты делаешь?<u>&lt;/P&gt;</u> <u>&lt;P&gt;</u>Как дела?<u>&lt;/P&gt;</u>
</div>
</div><br/>

То результат вам совсем не будет в строчку.<br/>
Каждая штучка захочет стоять на собственной строке, вдобавок еще и пустого пространства отхватит себе, для жадности:<br/><br/>
<div class="cd block">
<h3 id="hhh3" style="margin-top:0">Привет!</h3><p>А что ты делаешь?</p><p style="margin-bottom:0;padding-bottom:1em;">Как дела?</p>
</div><br/><br/> 

Помните я говорил, что нам не прийдется все время перескакивать на новую строчку тегом <u>&lt;BR&gt;</u>? За нас это будут делать жадные теги.
<br/><br/>


