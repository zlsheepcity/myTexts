<a name="monday" class="anchor"><h2 class="p"><span>§2. </span>Понедельник: самые простые и популярные</h2></a>

Любая школьница техникума «Интерьер и дизайн» знает что такое <u>&lt;b&gt;</u>, <u>&lt;i&gt;</u> и <u>&lt;a&gt;</u> — так что понедельничное занятие можно пропустить или же смешать с книгами Полины Дашковой и раннего Бродского. Хотя, конечно же, мы затронем сегодня не только перечисленные теги.<br/><br/>

Самые простые и популярные теги очень просты и весьма популярны:<br/><br/>

<div class="cd block">
	<i>(подошёл)</i><br/>
	— А у вас <a href="http://ru.wikipedia.org/wiki/%D0%9C%D0%B0%D0%BD%D0%B4%D0%B0%D1%80%D0%B8%D0%BD" alt="Мандарин в википедии" title="Мандарин в википедии">мандарины</a> с косточками?<br/>
	— Нет-нет, <b>специальный сорт без косточек.</b> Берите&nbsp;— очень сладкие.<br/>
	— Жаль.<br/>
	<i>(ушёл)</i>
</div><br/>


или то же самое, но с показанными тегами:
<div class="cd block">
	<u>&lt;I&gt;</u><i>(подошёл)</i><u>&lt;/I&gt;</u><u>&lt;BR&gt;</u><br/>
	— А у вас <u style="white-space:normal;">&lt;A href="http://en.wikipedia.org/<br/>wiki/Mandarin_orange"&gt;</u><a href="http://en.wikipedia.org/wiki/Mandarin_orange" alt="Мандарин в википедии" title="Мандарин в википедии">мандарины</a><u>&lt;/A&gt;</u> с&nbsp;косточками?<u>&lt;BR&gt;</u><br/>
	— Нет-нет, <u>&lt;B&gt;</u><b>специальный сорт без косточек.</b><u>&lt;/B&gt;</u> Берите&nbsp;— очень сладкие.<u>&lt;BR&gt;</u><br/>
	— Жаль.<u>&lt;BR&gt;</u><br/>
	<u>&lt;I&gt;</u><i>(ушёл)</i><u>&lt;/I&gt;</u><span class="starme">*</span>
</div>

<span class="starme">*</span>  Говорят, в мире есть несколько людей, которые могут читать текст вместе с написанными тегами и видеть результат прямо на сечатке глаза.
<br/><br/>

Давайте разберем наши мандарины по косточкам.
<br/><br/>

<h3>Жирный и курсив</h3>
Про тег <u>&lt;B&gt;</u> вы уже знаете (найдите его в примере)&nbsp;— весь&nbsp;обрамленный собою текст он делает жирным. Очень похож на него и тег <u>&lt;I&gt;</u> (italic) — текст становится <i>наклонным</i> или (более красивое имя) текст становится <i>курсивом.</i><br/><br/>

Это очень-очень простые теги, главное не забывайте про «горшочек не вари» и расставляйте закрывающие теги <nobr><u>&lt;/B&gt;</u> и <u>&lt;/I&gt;</u></nobr><br/><br/>

Это очень-очень популярные теги. Зачем вам нужно делать какие-то слова или фразы в тексте жирными и наклонными — решайте сами. А в последних лекциях, когда мы с вами будем подводить итоги, разведем нудные лекции о некоторых правилах приличия.
<br/><br/>

<h3>Ссылка</h3>
Ссылки во всем интернете делаются с помощью тега <u>&lt;A&gt;</u> <i>(anchor — якорь; на первый взгляд логика якорь-ссылка кажется непонятной, но есть некоторые логические предпосылки объясняющие эту морскую терминологию, что впрочем не делает… да, согласен, логика кажется непонятной).</i><br/><br/>

Как и любой порядочный тег, делает ссылкой весь текст внутри открывающих и закрывающих тегов:<br/>
<div class="cd"><u>&lt;A&gt;</u><a href="http://yetinews.com/">здесь могла бы быть ваша реклама</a><u>&lt;/A&gt;</u></div><br/>
Но логический вопрос «эй, а куда ведет нас эта ссылка?» заставляет нас делать сложности и писать <i class="name">атрибут HREF.</i><br/>
<div class="cd"><u>&lt;A HREF=http://www.yetinews.com&gt;</u><a href="http://yetinews.com/">здесь могла бы быть ваша реклама</a><u>&lt;/A&gt;</u></div><br/><br/>

Правила такие: в открывающем теге, после (названия тега) буквы <u>A</u>, после пробела, надо обязательно писать <u>HREF=</u> и  адрес, на который ведет наша ссылка. <b>Обязательно с волшебной комбинацией</b> <u>http://</u><br/><br/>

С ума сойти, да. Но после двух-трех пробных ссылок все эти правила, как правило, запоминаются.
<br/><br/>

<h3>(Этого не было в примере), большие и маленькие, штучки сверху и снизу</h3>

Для того чтобы вы уже сейчас могли щеголять своими познаниями, я вам кратко рассскажу о нескольких красивых тегах, которые не редко востребованы в жизни каждой волшебницы:<br/><br/>

<div class="cd block">
	<u>&lt;S&gt;</u><s>Этого не было в примере</s><u>&lt;/S&gt;</u><br/>
	<u>&lt;SMALL&gt;</u><small>ой, маленький текст</small><u>&lt;/SMALL&gt;</u><br/>
	<u>&lt;BIG&gt;</u><big>текст, чуть побольше обычного</big><u>&lt;/BIG&gt;</u><br/>
	штучка <u>&lt;SUP&gt;</u><sup>вверху</sup><u>&lt;/SUP&gt;</u><br/>
	и штучка <u>&lt;SUB&gt;</u><sub>внизу</sub><u>&lt;/SUB&gt;</u><br/>
	и особенный шик:
	<center><u>&lt;CENTER&gt;</u>текст размещенный по центру<u>&lt;/CENTER&gt;</u></center>
</div>
<br/>

<h3>Брр, новая строка</h3>

Самые прозорливые заметили в примере странный тег <u>&lt;BR&gt;</u> в каждой строчке. Это тег <i class="name">новая строка</i> (break).<br/><br/>

Если бы этого <u>&lt;BR&gt;</u> не было, то всё нами написанное будет вытягиваться в одну строчку (или перетекать со строчки на строчку, если текст длинный), даже если мы тысячу раз нажмем клавишу enter:<br/><br/>
<div class="cd block">
	<i>(подошёл)</i>
	— А у вас <a href="http://ru.wikipedia.org/wiki/%D0%9C%D0%B0%D0%BD%D0%B4%D0%B0%D1%80%D0%B8%D0%BD" alt="Мандарин в википедии" title="Мандарин в википедии">мандарины</a> с косточками?
	— Нет-нет, <b>специальный сорт без косточек.</b> Берите — очень сладкие.
	— Жаль.
	<i>(ушёл)</i>
</div><br/>

Кстати, дома перед родными можете попробовать следующий фокус: напишите в хтмл два слова и поставьте между ними десять… нет, пятнадцать пробелов. Теперь посмотрите результат. Оп! Только один пробел!<br/><br/> 

Зачем нужны такие фокусы? Оставим вопрос мужчинам-философам. Нам остаются факты: куча пробелов сливается в один, а для перехода на новую строчку надо каждый раз писать <u>&lt;BR&gt;</u> (кстати, этот тег холост и у него нет закрывающей пары).<br/><br/>

Для успокоения отмечу пару неплохих фактов:<br/><br/>

Во-первых, почти все наши с вами форумы, жжы и другие блоги, сайты знакомств и удовольствий — почти везде, где мы вводим тексты, умные роботы следят за нами и вместо каждого нажатия на клавишу enter вставляют невидимый <u>&lt;BR&gt;</u>. Это очень удобно, вы даже можете забыть о существовании такого тега. Но если не забудете, то в случае неожиданной поломки робота — вы первой окажетесь в дамках.<br/><br/>

Во-вторых, на самом деле, если очень правильно писать хтмл, то нам <i>никогда не надо будет переходить на новую строчку.</i> Но об этом в следующих лекциях.
